"""
run_heartbreaker_stress_tests.py
--------------------------------
Comprehensive leakage and proxy stress testing for the Heartbreaker multimodal model.
Runs 6 key ablation tests, 8 incremental metadata ladders, negative controls, and permutation testing.
"""

import os
import warnings
import numpy as np
import pandas as pd
import tensorflow as tf
from sklearn.model_selection import StratifiedKFold, train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import roc_auc_score, average_precision_score, confusion_matrix, roc_curve

# Local imports
from multimodal_data_prep import (
    build_metadata_matrix,
    fit_metadata_preprocessors,
    apply_metadata_preprocessors,
    load_and_cache_dataset,
)
from train_multimodal_ecg_model import (
    load_ecg_encoder,
    platt_calibrate_and_threshold,
    bootstrap_ci
)

warnings.filterwarnings("ignore")

META_CSV        = "data/subset_metadata_2000.csv"
SIGNAL_BASE_DIR = "data/raw"
ECG_MODEL_PATH  = "models/binary_1d_ecg_model.h5"
N_FOLDS         = 5
RANDOM_STATE    = 42
TARGET_SENS     = 0.85

def get_feature_mask(feature_names, required_groups):
    """
    Returns boolean mask of columns to keep based on keywords.
    Groups can be: 'demographic', 'axis', 'noise', 'missing', 'validated', 'workflow_flags', 'random', 'report'
    """
    mask = np.zeros(len(feature_names), dtype=bool)
    for i, name in enumerate(feature_names):
        if 'demographic' in required_groups and name in ['age', 'sex', 'bmi']:
            mask[i] = True
        if 'axis' in required_groups and name.startswith('axis_'):
            mask[i] = True
        if 'noise' in required_groups and name.startswith('has_'):
            mask[i] = True
        if 'missing' in required_groups and name.endswith('_missing'):
            mask[i] = True
        if 'validated' in required_groups and name == 'validated_by_human':
            mask[i] = True
        if 'workflow_flags' in required_groups and (name == 'validated_by_human' or name.startswith('has_') or name == 'noise_score'):
            mask[i] = True
        if 'random' in required_groups and name == 'random_noise':
            mask[i] = True
        if 'report' in required_groups and name.startswith('tfidf_'):
            mask[i] = True
    return mask

def main():
    print("="*60)
    print(" HEARTBREAKER STRESS TESTS & LEAKAGE ABLATION")
    print("="*60)

    # 1. Load data
    df_static, reports, y, patient_ids = build_metadata_matrix(META_CSV, include_text=True, tfidf_max_features=80)
    df_meta_csv = pd.read_csv(META_CSV)

    # Add random noise for negative control
    np.random.seed(RANDOM_STATE)
    df_static["random_noise"] = np.random.normal(size=len(df_static))

    X_signals, _, valid_indices = load_and_cache_dataset(META_CSV, SIGNAL_BASE_DIR)
    
    valid_mask = np.array(valid_indices)
    y = y[valid_mask]
    df_static = df_static.iloc[valid_mask].reset_index(drop=True)
    reports = reports.iloc[valid_mask].reset_index(drop=True)

    # Load clean Out-of-Fold ECG probabilities
    # (These were generated by training 5 distinct 1D ResNets inside strict nested folds)
    X_ecg_probs = np.load("models/clean_oof_ecg_probs.npy")

    skf = StratifiedKFold(n_splits=N_FOLDS, shuffle=True, random_state=RANDOM_STATE)

    # Define models
    model_configs = {
        # Standalone
        "ECG_Only": {"ecg": True, "groups": []},
        "Metadata_Only": {"ecg": False, "groups": ["demographic", "axis", "noise", "missing", "validated"]},
        "Report_Only": {"ecg": False, "groups": ["report"]},
        
        # Incremental Ladder
        "M1_Safe": {"ecg": True, "groups": ["demographic"]},
        "M3_Axis": {"ecg": True, "groups": ["demographic", "axis"]},
        "M4_Noise": {"ecg": True, "groups": ["demographic", "axis", "noise"]},
        "M5_Missing": {"ecg": True, "groups": ["demographic", "axis", "noise", "missing"]},
        "M6_Validated": {"ecg": True, "groups": ["demographic", "axis", "noise", "missing", "validated"]},
        "M7_Report": {"ecg": True, "groups": ["demographic", "axis", "noise", "missing", "validated", "report"]},
        
        # Negative Control
        "M6_NegativeControl": {"ecg": True, "groups": ["demographic", "axis", "noise", "missing", "validated", "random"]},
    }

    # Tracking OOF predictions
    oof_probs = {m: np.zeros(len(y)) for m in model_configs}
    # Permutation OOF tracking for M6 (Primary safe)
    perm_groups = ["demographic", "axis", "noise", "missing", "validated", "workflow_flags"]
    oof_perm_probs = {g: np.zeros(len(y)) for g in perm_groups}

    for fold, (train_idx, test_idx) in enumerate(skf.split(X_signals, y)):
        sub_train_idx, val_cal_idx = train_test_split(train_idx, test_size=0.20, stratify=y[train_idx], random_state=RANDOM_STATE)

        # 1. Fit Preprocessors
        X_meta_train_raw, prep = fit_metadata_preprocessors(
            df_static.iloc[sub_train_idx], reports.iloc[sub_train_idx], y[sub_train_idx],
            include_text=True, tfidf_max_features=80
        )
        X_meta_val = apply_metadata_preprocessors(df_static.iloc[val_cal_idx], reports.iloc[val_cal_idx], prep)
        X_meta_test = apply_metadata_preprocessors(df_static.iloc[test_idx], reports.iloc[test_idx], prep)

        # Feature Names
        static_cols = list(df_static.columns)
        tfidf_cols = [f"tfidf_{i}" for i in range(X_meta_train_raw.shape[1] - len(static_cols))]
        feature_names = static_cols + tfidf_cols

        for model_name, cfg in model_configs.items():
            # Build feature matrices
            mask = get_feature_mask(feature_names, cfg["groups"])
            
            X_tr = X_meta_train_raw[:, mask] if mask.sum() > 0 else np.zeros((len(sub_train_idx), 0))
            X_va = X_meta_val[:, mask] if mask.sum() > 0 else np.zeros((len(val_cal_idx), 0))
            X_te = X_meta_test[:, mask] if mask.sum() > 0 else np.zeros((len(test_idx), 0))

            if cfg["ecg"]:
                X_tr = np.hstack([X_ecg_probs[sub_train_idx].reshape(-1, 1), X_tr])
                X_va = np.hstack([X_ecg_probs[val_cal_idx].reshape(-1, 1), X_va])
                X_te = np.hstack([X_ecg_probs[test_idx].reshape(-1, 1), X_te])

            if X_tr.shape[1] == 0:
                continue

            # Train LR
            lr = LogisticRegression(max_iter=1000, C=0.5, random_state=RANDOM_STATE)
            lr.fit(X_tr, y[sub_train_idx])
            
            # Calibrate
            val_raw = lr.predict_proba(X_va)[:, 1]
            calibrator, thresh, val_cal = platt_calibrate_and_threshold(y[val_cal_idx], val_raw)
            
            # Predict
            test_raw = lr.predict_proba(X_te)[:, 1]
            test_cal = calibrator.predict_proba(test_raw.reshape(-1, 1))[:, 1]
            oof_probs[model_name][test_idx] = test_cal

            # If M6 (Primary structured model), run permutation tests
            if model_name == "M6_Validated":
                for p_group in perm_groups:
                    p_mask = get_feature_mask(feature_names, [p_group])
                    # Ensure p_mask is correct relative to the M6 mask
                    sub_mask = p_mask[mask]
                    
                    X_te_perm = X_te.copy()
                    ecg_offset = 1 if cfg["ecg"] else 0
                    
                    # Shuffle the columns belonging to this group jointly as a block
                    col_indices = [c_idx + ecg_offset for c_idx in range(len(sub_mask)) if sub_mask[c_idx]]
                    if len(col_indices) > 0:
                        perm_indices = np.random.permutation(len(X_te_perm))
                        X_te_perm[:, col_indices] = X_te_perm[perm_indices][:, col_indices]
                    
                    perm_test_raw = lr.predict_proba(X_te_perm)[:, 1]
                    perm_test_cal = calibrator.predict_proba(perm_test_raw.reshape(-1, 1))[:, 1]
                    oof_perm_probs[p_group][test_idx] = perm_test_cal

    # Evaluate Results
    print("\n\n" + "="*60)
    print(" ABLATION LADDER & STRESS TEST RESULTS")
    print("="*60)
    
    def evaluate(name, probs):
        auc = roc_auc_score(y, probs)
        pr = average_precision_score(y, probs)
        fpr, tpr, ths = roc_curve(y, probs)
        idx = np.where(tpr >= 0.85)[0][0]
        th = ths[idx]
        preds = (probs >= th).astype(int)
        cm = confusion_matrix(y, preds)
        tn, fp, fn, tp = cm.ravel()
        sens = tp / (tp+fn)
        spec = tn / (tn+fp)
        brier = float(np.mean((probs - y) ** 2))
        ci = bootstrap_ci(y, probs, preds)
        return auc, pr, sens, spec, brier, ci

    results = {}
    for name in ["ECG_Only", "Metadata_Only", "Report_Only", "M1_Safe", "M3_Axis", "M4_Noise", "M5_Missing", "M6_Validated", "M7_Report", "M6_NegativeControl"]:
        auc, pr, sens, spec, brier, ci = evaluate(name, oof_probs[name])
        results[name] = {"auc": auc, "pr": pr, "sens": sens, "spec": spec, "brier": brier, "ci": ci}
        print(f"{name:.<25} AUC: {auc:.4f} (95% CI: {ci['auc_ci'][0]:.4f}–{ci['auc_ci'][1]:.4f}) | PR: {pr:.4f} | Spec: {spec:.4f}")

    print("\n" + "="*60)
    print(" PERMUTATION TEST RESULTS (Drop in AUC from M6)")
    print("="*60)
    base_auc = results["M6_Validated"]["auc"]
    for p_group in perm_groups:
        p_auc, _, _, _, _, _ = evaluate(p_group, oof_perm_probs[p_group])
        drop = base_auc - p_auc
        print(f"Shuffled {p_group:.<15} AUC: {p_auc:.4f} (Drop: {drop:+.4f})")

    # Save to file
    with open("models/stress_test_results.txt", "w") as f:
        f.write("ABLATION LADDER\n")
        for name, m in results.items():
            f.write(f"{name:.<25} AUC: {m['auc']:.4f} (95% CI: {m['ci']['auc_ci'][0]:.4f}–{m['ci']['auc_ci'][1]:.4f}) | Sens: {m['sens']:.4f} | Spec: {m['spec']:.4f}\n")
        f.write("\nPERMUTATIONS\n")
        for p_group in perm_groups:
            p_auc = roc_auc_score(y, oof_perm_probs[p_group])
            f.write(f"Shuffled {p_group:.<15} AUC: {p_auc:.4f} (Drop: {base_auc - p_auc:+.4f})\n")

if __name__ == "__main__":
    main()
